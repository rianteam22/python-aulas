"""
Operadores aritmeticos
+ - soma
- - subtracao
* - multiplicacao
/ - divisao
// - divisao inteira
** - potenciacao
% - resto da divisao interia
() - ordem das contas
"""

print(10 * 10)
print(10 + 10)
print(10 - 10)
print(10 / 10)
print(10 // 3)
print(10 ** 10)
print(10 % 10)

# * serve como um operador de repeticao em string
print('10 ' * 10)
# + serve para concatenar strings
print('10' + '10')

print(5+2*10)
print((5+2)*10)