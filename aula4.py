"""
Tipos de dados
str - string
int - inteiro
float - ponto flutuante
bool - boolean

classes vazias sao avaliadas como False caso castadas em booleano

"""
print(type(1234), type('rian'), type(1 == 1), type(1.2))

# Nome - str
print('RIAN GALATAS MACEDO BRANDAO', type('RIAN GALATAS MACEDO BRANDAO'))

# Idade - int
print(20, type(20))

# Altura - float
print(1.66, type(1.66))

# Maior de idade - boolean
print(20 > 18, type(20 > 18))